# Python Built-in Data Types
# Python has the following built-in data types:
    # Text Type:	str
    # Numeric Types:	int, float, complex
    # Sequence Types:	list, tuple, range
    # Mapping Type:	dict
    # Set Types:	set, frozenset
    # Boolean Type:	bool
    # Binary Types:	bytes, bytearray, memoryview
    # None Type:	None

# Examples of Python Data Types

# Text Type (str)
text = "Hello, World!"

# Numeric Types
integer = 42
floating_point = 3.14
complex_number = 1 + 2j

# Sequence Types
my_list = [1, 2, 3, 4, 5]
my_tuple = (1, "two", 3.0)
my_range = range(10)

# Mapping Type
my_dict = {"name": "John", "age": 30}

# Set Types
my_set = {1, 2, 3, 4, 5}
my_frozenset = frozenset([1, 2, 3])

# Boolean Type
is_true = True
is_false = False

# Binary Types
my_bytes = b"Hello"
my_bytearray = bytearray(5)
my_memoryview = memoryview(bytes(5))

# None Type
my_none = None

# Printing examples
print("Text Type:", text)
print("Integer:", integer)
print("Float:", floating_point)
print("Complex:", complex_number)
print("List:", my_list)
print("Tuple:", my_tuple)
print("Range:", list(my_range))
print("Dictionary:", my_dict)
print("Set:", my_set)
print("Frozen Set:", my_frozenset)
print("Boolean:", is_true)
print("Bytes:", my_bytes)
print("ByteArray:", my_bytearray)
print("MemoryView:", my_memoryview)
print("None:", my_none)
